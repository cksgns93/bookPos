    private void btUpdateActionPerformed(java.awt.event.ActionEvent evt) {
    	//변경할 값들을 받아온다
    	String bCode =this.pCode.getText();
    	String bName =this.pName.getText();
    	//String값으로 된 price를 인티져로 int형 변환
    	int bPrice = Integer.parseInt(this.pPrice.getText());
    	//유효성 체크 null값이나 공백 검사
    	if(bCode==null||bCode.trim().isEmpty()||bName==null||bName.trim().isEmpty()||bPrice==0) {
    		showMsg("수정하실 책의 정보를 입력하세요!");
    		return;
    	}
    	//VO를 불러서 매개변수 입력
    	BookVO vo=new BookVO(bCode, bName, bPrice);
    	showMsg("수정이 완료되었습니다");
    	pCode.setText("");
    	pName.setText("");
    	pPrice.setText("");
    	
    	try {
			int result=dao.updateBook(vo);
			//bCode.
		} catch (SQLException e) {
			e.printStackTrace();
		}
    	showTable();
    }                                        

    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {                                         
    	String bCode = this.pCode.getText();
    	if(bCode==null||bCode.trim().isEmpty()) {
    		showMsg("삭제하실 책의 정보를 입력하세요!");
    		return;
    	}
    	dao=new BookDAO();
    	showMsg("삭제되었습니다");
    	pCode.setText("");
    	pName.setText("");
    	pPrice.setText("");
    	try {
			dao.deleteBook(bCode);
			
		} catch (SQLException e) {
			e.printStackTrace();
		}
    	showTable();
    }                                        

