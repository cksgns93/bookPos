    private void btSearchActionPerformed(java.awt.event.ActionEvent evt) {                                         
    	String barcode = pCode.getText();
    	BookDAO bdao = new BookDAO();
    	try {
			BookVO book = bdao.selectByBarcode(barcode).get(0);
			pCode.setText(book.getCode());
	        pLoc.setText("Loc N/A");
	        pName.setText(book.getName());
	        pPrice.setText(String.valueOf(book.getPrice()));
	        pStock.setText("stock N/A");
		} catch (Exception e) {
			// TODO Auto-generated catch block
			JOptionPane.showMessageDialog(this, "다음 바코드에 해당하는 책이 없습니다.");
			e.printStackTrace();
		}
    }                                        

    private void btInsertActionPerformed(java.awt.event.ActionEvent evt) {                                         
    	
    	String code = pCode.getText();
    	String name = pName.getText();
    	String price =pPrice.getText();
    	BookVO book = new BookVO(code, name, Integer.parseInt(price.trim()));
    	
    	BookDAO bDao = new BookDAO();
    	List<BookVO> arr;
		try {
			arr = bDao.selectBook();
			for(int i = 0;i < arr.size();i++) {
	    		if(arr.get(i).getCode().equals(code)) {
	    			JOptionPane.showMessageDialog(this, "해당 책의 수량이 추가 되었습니다");
	    			bDao.updateBook(book);//책의 수량 업데이트
	    			return;
	    		} 
	    	}
			JOptionPane.showMessageDialog(this, "새로운 책이 추가되었습니다");
	    	bDao.insertBook(book);
	    	pTable.setModel(new javax.swing.table.DefaultTableModel(
	                new Object [][] {
	                },
	                colHeader));
	    	showTable();
		} catch (SQLException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
    	
    	
    	
    	
    }                                        

